<?php
    // Inteiro
    $x = 4;
    print $x . ' ' . gettype($x) . "\n"; 
    
    // Decimal (Float ou Double)
    $x = 4.3;
    print $x . ' ' . gettype($x) . "\n";

    // String
    $x = 'teste';
    print $x . ' ' . gettype($x) . "\n";

    // Boolean
    $x = true;
    print $x . ' ' . gettype($x) . "\n";
?>
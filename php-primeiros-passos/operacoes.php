<?php
    // Soma
    $x = 3;
    $y = 5;
    print $x + $y . "\n"; // 8

    // Subtração
    $x = 5;
    $y = 1;
    print $x - $y . "\n"; // 4

    // Multiplicação
    $x = 5;
    $y = 4;
    print $x * $y . "\n"; // 20

    // Divisão
    $x = 6;
    $y = 3;
    print $x / $y . "\n"; // 2

    // Módulo
    $x = 15;
    $y = 14;
    print $x % $y . "\n"; // 1

    // Exponenciação
    $x = 2;
    $y = 4;
    print $x ** $y . "\n"; // 16
?>